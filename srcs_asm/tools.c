#include "../includes/asm.h"

int			ft_increment_tkn(char **tkn, int i, int *l)
{
	while (tkn[i] && ft_strcmp(tkn[i], "\n"))
		i++;
	if (!ft_strcmp(tkn[i], "\n"))
		(*l)++;
	i++;
	return (i);
}
